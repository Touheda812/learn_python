'''
Operators: + , - , * , /
Data Types: int, float
Division (/) which returns float but Floor Division (//) returns integer discarding any fractional result
Multiplication (*)
squared (**)

'''

# Floor division(//) vs division(/)
print(8 / 3)
print(8 // 3)

# Squared(**) vs multiplication(*)
print(2 * 3)
print(2 ** 3)



